package ejercicio02;

public class Piramide {
	
	// Atributos
	
	private double base;
	private double altura;
	
	// Constructor
	
	public Piramide (double base, double altura) {
		this.base = base;
		this.altura = altura;
	}
	
	// Getters & Setters
	
	public double getBase () {
		return base;
	}
	
	public void setBase (double base) {
		this.base = base;
	}
	
	public double getAltura () {
		return altura;
	}
	
	public void setAltura (double altura) {
		this.altura = altura;
	}

}
